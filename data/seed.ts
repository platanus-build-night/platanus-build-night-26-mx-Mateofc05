/** Wipe and reseed all reference data. Outreaches are cleared too. */
export async function seedDatabase(): Promise<{
  agencies: number;
  programs: number;
  coaches: number;
  athletes: number;
}> {
  // Delete in FK-safe order (children first).
  await db.delete(outreaches);
  await db.delete(athletesTable);
  await db.delete(coachesTable);
  await db.delete(programsTable);
  await db.delete(agencies);

  // Insert in FK-safe order (parents first).
  await db.insert(agencies).values({
    id: agency.id,
    name: agency.name,
    createdAt: new Date(agency.createdAt),
  });

  if (programs.length) {
    await db.insert(programsTable).values(
      programs.map((p) => ({
        id: p.id,
        school: p.school,
        division: p.division,
        conference: p.conference ?? null,
        state: p.state,
        sport: p.sport,
        level: p.level,
        rosterNeeds: p.rosterNeeds,
        intlFriendly: p.intlFriendly,
        academicMinGpa: p.academicMinGpa,
        notes: p.notes,
      })),
    );
  }

  if (coaches.length) {
    await db.insert(coachesTable).values(
      coaches.map((c) => ({
        id: c.id,
        programId: c.programId,
        name: c.name,
        title: c.title,
        email: c.email,
      })),
    );
  }

  if (athletes.length) {
    await db.insert(athletesTable).values(
      athletes.map((a) => ({
        id: a.id,
        agencyId: a.agencyId,
        fullName: a.fullName,
        country: a.country,
        nativeLanguage: a.nativeLanguage,
        isMinor: a.isMinor,
        parentalConsent: a.parentalConsent,
        sport: a.sport,
        position: a.position,
        positions: a.positions,
        gradYear: a.gradYear,
        heightCm: a.heightCm,
        dominantFoot: a.dominantFoot,
        gpaEquivalent: a.gpaEquivalent,
        stats: a.stats,
        videoUrl: a.videoUrl,
        englishTest: a.englishTest,
        ncaaCenterStatus: a.ncaaCenterStatus,
        createdAt: new Date(a.createdAt),
      })),
    );
  }

  return {
    agencies: 1,
    programs: programs.length,
    coaches: coaches.length,
    athletes: athletes.length,
  };
}
